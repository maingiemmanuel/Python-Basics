# list data structure
my_list = ["Kenya", "Nigeria", "Tanzania", "Brazil", "Sweden", "Uganda"]
my_num = [2, 8, 1, 34, 27, 9, 0, -1, 23]
my_num.sort()
my_list.sort()
print(my_list)
my_list[2] = "South Africa"
print(my_list[2])
print(f"I am from {my_list[0]}")
print(my_num)
# tuple data structure
my_tuple = ("Banana", "Pears", "Oranges", "Mangoes", "Watermelon", "Pineapple", "Apples")
print(my_tuple)

# set data structure
my_set = {"Toyota", "Nissan", "Mitsubishi", "Mercedes", "Subaru", "Audi"}
print(my_set)
my_set.add("Mazda")
# dictionaries data structure
my_dict = {"name": "Johnson", "age": "36", "gender": "Male"}
print(my_dict)
print(f"My age is {my_dict['age']}")
